package eu.kinstaitis.registration;

public class OrderEvent {

	private String wedding;
	private String christening;
	private String givingAname;
	private String party;
	private String conference;
	private String concert;
	private String photoshoot;
	private String modelPortfolio;
	private String event;

	public String getWedding() {
		return wedding;
	}

	public void setWedding(String wedding) {
		this.wedding = wedding;
	}

	public String getChristening() {
		return christening;
	}

	public void setChristening(String christening) {
		this.christening = christening;
	}

	public String getGivingAname() {
		return givingAname;
	}

	public void setGivingAname(String givingAname) {
		this.givingAname = givingAname;
	}

	public String getParty() {
		return party;
	}

	public void setParty(String party) {
		this.party = party;
	}

	public String getConference() {
		return conference;
	}

	public void setConference(String conference) {
		this.conference = conference;
	}

	public String getConcert() {
		return concert;
	}

	public void setConcert(String concert) {
		this.concert = concert;
	}

	public String getPhotoshoot() {
		return photoshoot;
	}

	public void setPhotoshoot(String photoshoot) {
		this.photoshoot = photoshoot;
	}

	public String getModelPortfolio() {
		return modelPortfolio;
	}

	public void setModelPortfolio(String modelPortfolio) {
		this.modelPortfolio = modelPortfolio;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}

}
